
df <- data.frame(mass = c(0, 10, 20, 30, 40),
                 F1 = c(0, 49.8, 99.6, 120.2, 310.4))

df %>%
  mutate(
    F1_mass = case_when(

      # g-g
      F1 >= analog1 & F1 < analog2 ~ (mass2-mass1)/(analog2-analog1)*(F1-analog1) + mass1

      # 0g-20g
      F1 >= 0 & F1 < 99.6 ~ (20-0)/(99.6-0)*F1,
      # 20g-30g
      F1 >= 99.6 & F1 < 120.2 ~ (30-20)/(120.2-99.6)*(F1-99.6) + 20,
      # 30g-40g
      F1 >= 120.2 & F1 < 310.4 ~ (40-30)/(310.4-120.2)*(F1-120.2) + 30,
      # 40g-50g
      F1 >= 310.4 & F1 < 435.8 ~ (50-40)/(435.8-310.4)*(F1-310.4) + 40,
      # 50g-60g
      F1 >= 435.8 & F1 < 446 ~ (60-50)/(446-435.8)*(F1-435.8) + 50

    )
  )


